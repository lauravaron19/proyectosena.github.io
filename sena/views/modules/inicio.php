    <h1 class="text-center">¿Que es la  quimica ?<h1>

     <div class="text-center">
         <img class="img-fluid" loading="lazy" src="https://img.freepik.com/foto-gratis/elementos-ciencia-alto-angulo-disposicion-productos-quimicos_23-2148697180.jpg?size=626&ext=jpg">
     </div>

     <p>
        La química es una rama de la ciencia que se dedica a estudiar los elementos por sí solos, pero también las formas en que estos interactúan con otros átomos del mismo elemento o con átomos de elementos diferentes.
    </p>
    <img class="img-fluid" loading="lazy" src="https://plustatic.com/1378/conversions/ramas-quimica-square.jpg">
    <p>
        Es una ciencia que no propende a ofrecerse en espectáculo, pero sin ella muchas proezas terapéuticas, hazañas espaciales y maravillas de la técnica, que todos consideramos espectaculares, no habrían visto la luz del día. 
        No olvidemos que la química ayuda a los historiadores del arte a descubrir algunos de los secretos de fabricación de los cuadros y esculturas que admiramos en los museos. 
        Recordemos asimismo que permite a la policía científica analizar las muestras recogidas en el "Escenario del delito" e identificar así a los culpables más rápidamente, y por último sepamos también que es ella 
        la que descubre las sutilezas moleculares de los platos que cautivan nuestro paladar. Junto con la física, que descifra las leyes del universo, y la biología, que descodifica las reglas de la vida, la química es la ciencia de la materia y de su 
        transformaciones. Desde la auténtica revolución que supuso la síntesis de la urea, lograda en 1828, que demostró la posibilidad de obtener una molécula orgánica a partir de un compuesto mineral, hasta la consecución de la síntesis de la vitamina B12 en el decenio de 1970, esta disciplina ha ido consolidando continuamente su poder sobre la estructura y la transformación de la materia.La molécula como caballo de Troya Más allá de la química molecular se extiende el inmenso ámbito de la llamada
        química supramolecular, que no estudia lo que ocurre dentro de las moléculas, sino más bien cómo éstas se conducen entre sí. Su objetivo es comprender y controlar su modo de interacción y la manera en que se transforman y unen, ignorando a otras moléculas. En el ámbito de la biología es donde más sorprendente resulta el papel de las interacciones moleculares: las unidades proteínicas que se unen para formar la hemoglobina; los glóbulos blancos que reconocen y destruyen los cuerpos extraños; el virus del sida que encuentra su blanco y se introduce en él; el código genético que se transmite
        mediante la escritura y lectura del alfabeto de las bases proteínicas, etc. La eficacia y elegancia de los fenómenos naturales son tan fascinantes para un químico que su tentación es tratar de reproducirlos, o de inventar nuevos procedimientos que permitan crear nuevas arquitecturas moleculares con aplicaciones múltiples. Las partículas formaron átomos y éstos se unieron para producir moléculas cada vez más complejas que, a su vez, se asociaron en agregados y membranas dando así a luz a las primeras células de las que brotó la vida en nuestro planeta. La tarea de la química es revelar las vías de la auto organización y trazar los caminos que conducen de la materia inerte -a través de una evolución prebiótica puramente química- al nacimiento de la vida, y de aquí a la materia viva, y luego a la materia pensante.
    </p>
