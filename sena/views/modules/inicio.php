           <img class="img-fluid" loading="lazy" src="https://www.espaciohonduras.net/images/Quimica/articulos/Quimica_organica/Quimica_organica.jpg">
        </div>
        
       <p>
        la quimica organica es la que estudia la reaccion quimica,propiedades y comportamientos de las estructuras basadas en moleculas de carbono  
        </P>
        <img class="img-fluid" loading="lazy" src="https://prod-discovery.edx-cdn.org/media/course/image/310524cf-615f-42a7-b3ee-1140ca332e33-ad6bbbabb1ce.small.jpg">
        <p>
        la quimica  organica como campo de estudio se interesa por la estructura,el comportamiento,las propiedades  y los usos  de este tipo de compuestos quimicos,para la quimica  moderna,los elementos que forman los compuestos organicos son aquellos que usualmente aparecen en los organimos vivientes
        y en sus compuestos derivados, como el carbono (C), el hidrogeno (H), el azufre (S), el oxigeno (O), el nitrogeno (N) y todos los elementos halogenos.
        El origen  del nombre "quimica organica" proviene de ciertas teorias científicas que estuvieron en boga hasta mediados del siglo XlX, y que proponian  que los compuestos organicos  eran necesariamente, restos o residuos de antiguos seres vivos.Por lo tanto,sostenian que todas la materia organica
        provenia de sus cuerpos, sin embargo, en 1828 el qumico aleman FRIEDRICH WOHLER se dio cuenta de que sustancias inorganicas como el cianato de amonio(CH4N2O) podian convertise, mediante determinados procesos quimicos, en una sustancia organica como es la urea, que forma parte de la orina de numerosos animales,por ejemplo 
        WOHLER obtuvo la primera evidencia de que la materia organica y inorganica podian tener un origen comun, no relacionado necesariamente con la vida
